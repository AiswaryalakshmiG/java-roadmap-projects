package operators;

public class AssignmentOperator {
public static void main(String[] args) {
	int num1= 10;
	num1 += 5;
	System.out.println(num1);
	num1 *=2;
	System.out.println(num1);
	num1 -=2;
	System.out.println(num1);
	num1 /=3;
	System.out.println(num1);
	num1 %=2;
	System.out.println(num1);
}
}
